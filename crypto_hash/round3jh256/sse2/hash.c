    SS(x0,x2,x4,x6,x1,x3,x5,x7,LOAD_ALIGNED((__m128i*)c[r+4]),LOAD_ALIGNED(1+(__m128i*)c[r+4]));
    L(x0,x2,x4,x6,x1,x3,x5,x7);
    SWAP16(x1); SWAP16(x3); SWAP16(x5); SWAP16(x7);
    SS(x0,x2,x4,x6,x1,x3,x5,x7,LOAD_ALIGNED((__m128i*)c[r+5]),LOAD_ALIGNED(1+(__m128i*)c[r+5]));
